CREATE TABLE requirement (
    id VARCHAR(20) PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    summary CLOB,
    business_justification CLOB,
    priority VARCHAR(32) NOT NULL,
    status VARCHAR(32) NOT NULL,
    category VARCHAR(32) NOT NULL,
    source VARCHAR(32) NOT NULL,
    assignee VARCHAR(128),
    completeness_score INT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    workspace_id VARCHAR(64) NOT NULL
);

CREATE TABLE requirement_acceptance_criterion (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    criterion_key VARCHAR(32) NOT NULL,
    criterion_text CLOB NOT NULL,
    is_met BOOLEAN NOT NULL,
    display_order INT NOT NULL,
    CONSTRAINT fk_req_ac_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_assumption (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    assumption_text CLOB NOT NULL,
    display_order INT NOT NULL,
    CONSTRAINT fk_req_assumption_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_constraint (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    constraint_text CLOB NOT NULL,
    display_order INT NOT NULL,
    CONSTRAINT fk_req_constraint_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE user_story (
    id VARCHAR(20) PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    title VARCHAR(255) NOT NULL,
    status VARCHAR(32) NOT NULL,
    spec_id VARCHAR(20),
    spec_status VARCHAR(32),
    display_order INT NOT NULL,
    CONSTRAINT fk_user_story_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_spec (
    id VARCHAR(20) PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    title VARCHAR(255) NOT NULL,
    status VARCHAR(32) NOT NULL,
    version VARCHAR(32) NOT NULL,
    display_order INT NOT NULL,
    CONSTRAINT fk_req_spec_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_ai_analysis (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL UNIQUE,
    completeness_score INT NOT NULL,
    impact_assessment CLOB NOT NULL,
    analyzed_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_req_analysis_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_ai_analysis_element (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    analysis_id BIGINT NOT NULL,
    element_type VARCHAR(32) NOT NULL,
    element_text CLOB NOT NULL,
    related_requirement_id VARCHAR(20),
    numeric_value DECIMAL(8,2),
    display_order INT NOT NULL,
    CONSTRAINT fk_req_analysis_element_analysis FOREIGN KEY (analysis_id) REFERENCES requirement_ai_analysis (id)
);

CREATE TABLE requirement_sdlc_chain_link (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20) NOT NULL,
    artifact_type VARCHAR(32) NOT NULL,
    artifact_id VARCHAR(32) NOT NULL,
    artifact_title VARCHAR(255) NOT NULL,
    route_path VARCHAR(255) NOT NULL,
    link_order INT NOT NULL,
    CONSTRAINT fk_req_chain_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

CREATE TABLE requirement_import_audit (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requirement_id VARCHAR(20),
    source_format VARCHAR(32) NOT NULL,
    file_name VARCHAR(255),
    file_size BIGINT,
    skill_id VARCHAR(64) NOT NULL,
    username VARCHAR(128) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    outcome VARCHAR(32) NOT NULL,
    CONSTRAINT fk_req_import_audit_requirement FOREIGN KEY (requirement_id) REFERENCES requirement (id)
);

