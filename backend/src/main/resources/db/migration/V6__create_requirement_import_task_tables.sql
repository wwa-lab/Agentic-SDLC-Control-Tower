CREATE TABLE requirement_import_task (
    id VARCHAR(64) PRIMARY KEY,
    kb_name VARCHAR(255) NOT NULL,
    dataset_id VARCHAR(128),
    provider_name VARCHAR(64) NOT NULL,
    profile_id VARCHAR(64) NOT NULL,
    status VARCHAR(32) NOT NULL,
    message CLOB,
    total_number_of_files INT NOT NULL,
    number_of_successes INT NOT NULL,
    number_of_failures INT NOT NULL,
    total_size BIGINT NOT NULL,
    supported_file_types CLOB,
    unsupported_file_types CLOB,
    draft_payload_json CLOB,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    completed_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE requirement_import_file (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    import_id VARCHAR(64) NOT NULL,
    display_name VARCHAR(512) NOT NULL,
    source_file_name VARCHAR(255) NOT NULL,
    source_kind VARCHAR(32) NOT NULL,
    file_extension VARCHAR(32),
    content_type VARCHAR(128),
    file_size BIGINT NOT NULL,
    supported BOOLEAN NOT NULL,
    provider_document_id VARCHAR(128),
    provider_batch_id VARCHAR(128),
    provider_status VARCHAR(32) NOT NULL,
    error_message CLOB,
    extracted_preview CLOB,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    completed_at TIMESTAMP WITH TIME ZONE,
    CONSTRAINT fk_requirement_import_file_task FOREIGN KEY (import_id) REFERENCES requirement_import_task (id)
);
