const sparklineRunIds: Record<string, ReadonlyArray<string>> = {
  'repo-1': ['run-992', 'run-991', 'run-993'],
  'repo-2': ['run-995'],
  'repo-3': ['run-993'],
  'repo-4': ['run-996'],
  'repo-5': ['run-994'],
  'repo-7': ['run-996'],
  'repo-8': ['run-994'],
  'repo-9': ['run-996'],
  'repo-10': ['run-993'],
  'repo-11': ['run-994'],
  'repo-12': ['run-995'],
};

function sparkline(prefix: string, statuses: ReadonlyArray<RunStatus>) {
  const runIds = sparklineRunIds[prefix] ?? ['run-991'];
  return statuses.map((status, index) => ({
    runId: runIds[index % runIds.length],
    status,
    label: `${prefix} sample ${index + 1}`,
  }));
}

function repo(row: Omit<CatalogRepoRow, 'sparkline'> & { sparklineStatuses: ReadonlyArray<RunStatus> }): CatalogRepoRow {
  return {
    ...row,
    sparkline: sparkline(row.repoId, row.sparklineStatuses),
  };
}

